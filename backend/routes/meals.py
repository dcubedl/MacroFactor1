@router.post("/meals/shopping-list", response_model=ShoppingListResponse)
async def create_shopping_list_route(
    body: ShoppingListCreateRequest,
    user_id: str = Depends(get_current_user),
):
    """
    Create a shopping list from a recipe.

    Provide `already_have` (list of ingredient names) to pre-mark items the
    user already owns.  Optionally link to a `scan_id` for later comparison.
    """
    # Verify recipe exists
    recipe = await get_recipe_with_ingredients(body.recipe_id)
    if not recipe:
        raise HTTPException(status_code=404, detail="Recipe not found.")

    # Verify scan ownership (if provided)
    if body.scan_id:
        scan = await get_food_scan_by_id(user_id, body.scan_id)
        if not scan:
            raise HTTPException(
                status_code=404,
                detail="Food scan not found or does not belong to you.",
            )

    shopping_list = await create_shopping_list(
        user_id=user_id,
        recipe_id=body.recipe_id,
        scan_id=body.scan_id,
        already_have=body.already_have,
        ingredients=recipe.get("ingredients", []),
    )
    return ShoppingListResponse(**shopping_list)


@router.put("/meals/shopping-list/{list_id}", response_model=ShoppingListResponse)
async def update_shopping_list_route(
    list_id: str,
    body: ShoppingListUpdateRequest,
    user_id: str = Depends(get_current_user),
):
    """
    Update which ingredients the user already has at home.

    Accepts a list of `{ingredient_name, already_have}` objects.  Only items
    belonging to a shopping list owned by the authenticated user are updated.
    """
    existing = await get_shopping_list(user_id, list_id)
    if not existing:
        raise HTTPException(
            status_code=404,
            detail="Shopping list not found or does not belong to you.",
        )

    updates = {item.ingredient_name: item.already_have for item in body.items}
    updated = await update_shopping_list_items(list_id, updates)
    return ShoppingListResponse(**updated)


