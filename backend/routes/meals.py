@router.get(
    "/meals/shopping-list/{list_id}/comparison",
    response_model=MealComparisonResponse,
)
async def meal_comparison(
    list_id: str,
    scan_id: str = Query(..., description="ID of the original food scan to compare against"),
    user_id: str = Depends(get_current_user),
):
    """
    Side-by-side macro comparison between a recipe and a past food scan.

    Returns both macro profiles plus a list of human-readable improvement
    strings (e.g. "2× the protein", "40% less fat").
    """
    # Fetch shopping list (ownership check)
    shopping_list = await get_shopping_list(user_id, list_id)
    if not shopping_list:
        raise HTTPException(
            status_code=404,
            detail="Shopping list not found or does not belong to you.",
        )

    # Fetch recipe
    recipe_id = shopping_list.get("recipe_id")
    recipe = await get_recipe_with_ingredients(recipe_id)
    if not recipe:
        raise HTTPException(status_code=404, detail="Recipe not found.")

    # Fetch original scan (ownership check)
    scan = await get_food_scan_by_id(user_id, scan_id)
    if not scan:
        raise HTTPException(status_code=404, detail="Food scan not found.")

    improvements = generate_comparison(scan, recipe)

    return MealComparisonResponse(
        original_meal=MealSideResponse(
            name=scan.get("food_name", "Original meal"),
            calories=scan.get("calories"),
            protein_g=scan.get("protein_g"),
            carbs_g=scan.get("carbs_g"),
            fat_g=scan.get("fat_g"),
            fiber_g=scan.get("fiber_g"),
        ),
        new_meal=MealSideResponse(
            name=recipe.get("name", "Recipe"),
            calories=recipe.get("calories"),
            protein_g=recipe.get("protein_g"),
            carbs_g=recipe.get("carbs_g"),
            fat_g=recipe.get("fat_g"),
            fiber_g=recipe.get("fiber_g"),
        ),
        improvements=improvements,
    )


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------

def _to_recipe_response(r: dict) -> dict:
    """
    Normalise a recipe row from Supabase into the RecipeResponse field set.

    Gemini-generated recipes may be missing ``id`` and ``is_curated``; supply
    safe defaults so the Pydantic model never raises a validation error.
    """
    return {
        "id":                r.get("id", ""),
        "name":              r.get("name", ""),
        "cuisine":           r.get("cuisine", ""),
        "description":       r.get("description"),
        "calories":          r.get("calories"),
        "protein_g":         r.get("protein_g"),
        "carbs_g":           r.get("carbs_g"),
        "fat_g":             r.get("fat_g"),
        "fiber_g":           r.get("fiber_g"),
        "prep_time_minutes": r.get("prep_time_minutes"),
        "servings":          r.get("servings", 1),
        "image_url":         r.get("image_url"),
        "is_curated":        r.get("is_curated", False),
    }
