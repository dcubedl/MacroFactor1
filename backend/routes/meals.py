"""
Meal improvement and recipe suggestion routes.

NOTE: Routes marked as premium-only — paywall check to be added when Stripe
is integrated.

Endpoints
---------
GET  /api/meals/suggestions?scan_id={id}
    Fetch improvement tips and curated/AI recipe suggestions for a past scan.

GET  /api/meals/recipes?cuisine={cuisine}&limit=10
    Browse the curated recipe library, optionally filtered by cuisine.

GET  /api/meals/recipes/{id}
    Full recipe detail including ingredient list.

POST /api/meals/shopping-list
    Create a shopping list from a recipe (optionally linked to a scan).

PUT  /api/meals/shopping-list/{id}
    Update which ingredients the user already has at home.

GET  /api/meals/shopping-list/{id}/comparison?scan_id={id}
    Side-by-side macro comparison of a recipe vs. a past food scan.
"""

from typing import Optional

from fastapi import APIRouter, Depends, HTTPException, Query

from models.schemas import (
    MealComparisonResponse,
    MealSideResponse,
    MealSuggestionsResponse,
    RecipeDetailResponse,
    RecipeResponse,
    ShoppingListCreateRequest,
    ShoppingListResponse,
    ShoppingListUpdateRequest,
)
from services.auth import require_premium as get_current_user  # All meals routes are PREMIUM-ONLY
from services.meal_suggestions import (
    generate_comparison,
    generate_improvement_tips,
    suggest_recipes,
)
from database.supabase import (
    create_shopping_list,
    get_curated_recipes_for_suggestions,
    get_food_scan_by_id,
    get_recipe_with_ingredients,
    get_recipes,
    get_shopping_list,
    update_shopping_list_items,
)

router = APIRouter()


# ---------------------------------------------------------------------------
# Suggestions
# ---------------------------------------------------------------------------

@router.get("/meals/suggestions", response_model=MealSuggestionsResponse)
async def meal_suggestions(
    scan_id: str = Query(..., description="ID of a previous food scan"),
    user_id: str = Depends(get_current_user),
):
    """
    Return improvement tips and healthier recipe alternatives for a past scan.

    The scan must belong to the authenticated user.  Tips are generated by
    Gemini; recipes come from the curated library first, with Gemini filling
    in the gaps when fewer than 3 curated matches beat the scan score.
    """
    scan = await get_food_scan_by_id(user_id, scan_id)
    if not scan:
        raise HTTPException(status_code=404, detail="Food scan not found.")

    # Fetch curated recipes (cuisine-filtered when possible)
    available = await get_curated_recipes_for_suggestions(scan.get("food_name", ""))

    tips, recipes = await _gather_suggestions(scan, available)

    return MealSuggestionsResponse(
        improvement_tips=tips,
        suggested_recipes=[RecipeResponse(**_to_recipe_response(r)) for r in recipes],
    )


async def _gather_suggestions(scan: dict, available: list[dict]):
    """Run tips and recipe generation concurrently."""
    import asyncio
    tips_coro    = generate_improvement_tips(scan)
    recipes_coro = suggest_recipes(scan, available, n=5)
    tips, recipes = await asyncio.gather(tips_coro, recipes_coro)
    return tips, recipes


