@router.get("/meals/recipes", response_model=list[RecipeResponse])
async def browse_recipes(
    cuisine: Optional[str] = Query(default=None, description="Filter by cuisine"),
    limit: int = Query(default=10, ge=1, le=50),
    user_id: str = Depends(get_current_user),
):
    """
    Browse the curated recipe library.

    Optionally filter by cuisine (indian, arab, japanese, italian, mexican,
    chinese, british, korean).  Returns at most `limit` recipes.
    """
    rows = await get_recipes(cuisine=cuisine, limit=limit)
    return [RecipeResponse(**_to_recipe_response(r)) for r in rows]


@router.get("/meals/recipes/{recipe_id}", response_model=RecipeDetailResponse)
async def get_recipe(
    recipe_id: str,
    user_id: str = Depends(get_current_user),
):
    """Return full recipe detail including the ingredient list."""
    row = await get_recipe_with_ingredients(recipe_id)
    if not row:
        raise HTTPException(status_code=404, detail="Recipe not found.")

    ingredients = row.pop("ingredients", []) or []
    return RecipeDetailResponse(
        **_to_recipe_response(row),
        ingredients=ingredients,
    )


